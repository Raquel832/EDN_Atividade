# 4- Calculadora de Preço Total
# * Desenvolva um programa que calcula o preço total de uma compra. Use as seguintes informações:
# * Nome do produto: "Cadeira Infantil"
# * Preço unitário: R$ 12.40
# * Quantidade: 3
# O programa deve calcular o preço total e exibir todas as informações, incluindo o resultado final.

Nome_produto = "Cadeira Infantil"
Preço_unitário = 12.40
Quantidade = 3

Preço_total = Preço_unitário * Quantidade

print("===Calculadora de preço===")

print("Produto:", Nome_produto)

print("Preço unitário R$:", Preço_unitário)

print("Quantidade:", Quantidade)

print("Preço total é R$:", Preço_total)
